<template>
  <Modal 
    v-model='showDlg'
    :transfer='true'
    :width='420'
    :title="!id ? '新增院线信息' : '编辑院线信息'"
    @on-cancel="cancel('dataForm')" >
    <Form ref="dataForm" :model="dataForm" label-position="left" :rules="ruleValidate" :label-width="100">
        <FormItem label="名称" prop="name">
            <Input v-model="dataForm.name"></Input>
        </FormItem>
        <FormItem label="简称" prop="shortName">
            <Input v-model="dataForm.shortName"></Input>
        </FormItem>
        <FormItem label="拼音" prop="pinyin">
            <Input v-model="dataForm.pinyin"></Input>
        </FormItem>
        <FormItem label="状态" prop="Status">
            <RadioGroup v-model="dataForm.Status">
                <Radio label="1">正常</Radio>
                <Radio label="2">停业</Radio>
            </RadioGroup>
        </FormItem>
        <FormItem label="控制状态" prop="chainControlStatus">
            <RadioGroup v-model="dataForm.chainControlStatus">
                <Radio label="1">已上架</Radio>
                <Radio label="2">已下架</Radio>
            </RadioGroup>
        </FormItem>
    </Form>
    <div  slot="footer" class="dialog-footer">
      <Button @click="cancel('dataForm')">取消</Button>
      <Button type="primary" @click="dataFormSubmit('dataForm')">确定</Button>
    </div>
  </Modal>
</template>

<script lang="ts">
// doc: https://github.com/kaorun343/vue-property-decorator
import { Component, Prop } from 'vue-property-decorator'
import { dataFrom , add , set} from '@/api/cinemaChain'
import { warning , success } from '@/ui/modal'
import View from '@/util/View'

const dataForm = {
  name: '',
  shortName: '',
  pinyin: '',
  Status: '',
  chainControlStatus: ''
}

@Component
export default class ComponentMain extends View {
  showDlg = false
  id = 0
  ruleValidate = {
    name: [
        { required: true, message: '请输入院线名称', trigger: 'blur' }
    ],
    shortName: [
        { required: true, message: '请输入院线简称', trigger: 'blur' }
    ],
    pinyin: [
        { required: true, message: '请输入院线拼音', trigger: 'blur' }
    ],
    Status: [
        { required: true, message: '请选择院线状态', trigger: 'change' }
    ],
    chainControlStatus: [
        { required: true, message: '请选择院线控制状态', trigger: 'change' }
    ]
  }
  dataForm = { ...dataForm }
  init(id: number) {
    this.showDlg = true
    this.id = id || 0
    this.$nextTick(async () => {
      const dataForms: string = 'dataForm'
      const myThis: any = this
      myThis.$refs[dataForms].resetFields()
      if (this.id) {
        const {data: {
          items: list
        }} = await dataFrom({ id })
      }
    })
  }

  cancel(dataForms: string) {
    this.showDlg = false
    const myThis: any = this
    myThis.$refs[dataForms].resetFields()
  }

  // 表单提交
  dataFormSubmit(dataForms: any) {
   const myThis: any = this
   myThis.$refs[dataForms].validate(async ( valid: any ) => {
      if (valid) {
        const query = !this.id ? this.dataForm : {
          id: this.id,
          ...this.dataForm
        }
        const title = !this.id ? '添加' : '编辑'
        const res = !this.id ? await add (query) : await set (query)
        if ( res && res.code === 0 ) {
          this.$Message.success({
              content: `${title}成功`,
              onClose: () => {
                this.showDlg = false
                this.$emit('refreshDataList')
              }
          })
        } else {
          this.$Message.success({
            content: `${title}失败`
          })
        }
      }
    })
  }

}
</script>

