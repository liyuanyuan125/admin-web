<script lang="ts">
// doc: https://github.com/kaorun343/vue-property-decorator
import { Component, Watch } from 'vue-property-decorator'
import View from '@/util/View'
import { queryId, addSeach, addQuery } from '@/api/corpReal'
import AreaSelect from '@/components/AreaSelect.vue'
import PartBindCinema from './partBindCinema.vue'
import Upload from './upload.vue'
import { toMap } from '@/fn/array'
import { slice, clean } from '@/fn/object'

const makeMap = (list: any[]) => toMap(list, 'key', 'text')

const defItem = {
  typearr: [false, false],
  cinemasList: [],
  name: '',
  shortName: '',

  aptitudeNo: '',

  provinceId: 0,
  cityId: 0,
  countyId: 0,
  addressDetail: '',

  contact: '',
  contactTel: '',

  email: '',

  qualificationType: '',
  qualificationCode: '',
  images: [],
  types: [{
    typeCode: '',
    typeCategoryCode: ''
  },
  {
    typeCode: '',
    typeCategoryCode: ''
  }],
  refusedReason: '',
  levelCode: '',
  businessDirector: 1,
  cinemas: [],
  approveStatus: 1,
  validityPeriodDate: ''
}

@Component({
  components: {
    AreaSelect,
    PartBindCinema,
    Upload
  }
})

export default class Main extends View {
  title = ''
  loading = false
  loadingShow = false
  item: any = {...defItem}
  shows = true
  b: any = {}
  levelList = []
  customerTypeList = []
  bizUserList = []
  typeList = []
  typeListSubMap = {}
  profitUnitList = []
  profitTypeList = []
  area: number[] = []
  typerule = [
        {  message: '请选择客户等级', trigger: 'change'}
      ]
  get rules() {
    const validateType1 = ( rule1: any, value: any, callback: any) => {
      if (value == false) {
        callback(new Error('请选择一级类型'))
      } else {
        if (!this.item.types[0].typeCategoryCode) {
          callback(new Error('请选择二级类型'))
        } else {
          callback()
        }
      }
    }
    const validateType2 = ( rules: any, value: any, callback: any) => {
      if (value == false) {
        callback()
      } else {
        if (!this.item.types[1].typeCategoryCode) {
          callback(new Error('请选择二级类型'))
        } else {
          callback()
        }
      }
    }
    const rule: any = {
      name: [
        { required: true, message: '请填写公司名称', trigger: 'blur' }
      ],
      shortName: [
        { required: true, message: '请填写公司简称', trigger: 'blur' }
      ],
      provinceId: [
        { required: true, pattern: /^(?!0\d)[1-9]\d+/, message: '请选择公司地址', trigger: 'change' }
      ],
      addressDetail: [
        { required: true, message: '请填写公司详细地址', trigger: 'blur' }
      ],
      approveStatus: [
        { required: true, message: '请选择审核状态', trigger: 'blur', type: 'number' }
      ],
      cinemasList: [
        { required: true, message: '请选择关联影院', type: 'array', trigger: 'change'}
      ],
      levelCode: [
        { required: true, message: '请选择客户等级', trigger: 'change'}
      ],
      validityPeriodDate: [
          { required: true, message: '请填写用户的资质到期日期', trigger: 'change', type: 'date'}
      ],
      refusedReason: [
         { required: true, message: '请填写拒绝原因', trigger: 'blur'}
      ],
      email: [
         {
           pattern: /^([a-zA-Z0-9_-])+@([a-zA-Z0-9_-])+((.[a-zA-Z0-9_-]{2,3}){1,2})$/,
           message: '邮箱格式错误',
           trigger: 'blur'
         }
      ],
      'typearr[0]': [
        { required: true, message: '请选择一级客户', type: 'boolean', trigger: 'change'},
        { validator: validateType1 }
      ],
      'typearr[1]': [
        { validator: validateType2 }
      ]
    }
    return rule
  }

  created() {
    if (this.$route.params) {
      this.item.typearr = [false, false]
      this.item.types = this.item.types.map((val: any) => {
        return {
          typeCode: '',
          typeCategoryCode: ''
        }
      })
    }
  }

  typeCode(val: any, index: any) {
    if (!this.item.typearr[index]) {
      this.item.types[index].typeCode = val
    } else {
      this.item.types[index].typeCode = ''
      this.item.types[index].typeCategoryCode = ''
    }
  }

  get cinematype() {
    if (this.item.types[1].typeCategoryCode == 'cinema') {
      return 1
    } else {
      return 0
    }
  }
  get cinemas() {
    if (this.item.cinemasList.length > 0) {
      const cinemas = this.item.cinemasList.map((val: any) => {
        return val.id * 1
      })
      return cinemas
    } else {
      return []
    }
  }

  get imgList() {
    if ( this.item.images.length > 0 ) {
      return this.item.images.map((item: string) => {
        return {
          imageUrl: item,
          status: 'finished'
        }
      })
    } else {
      return []
    }

  }

  get cachedMap() {
    return {
      levelList: makeMap(this.levelList),
    }
  }

  get formatArr() {
    const cachedMap = this.cachedMap
    return {
      levelText: cachedMap.levelList[this.item.levelCode],
    }
  }

  mounted() {
    this.load()
  }

  async load() {
    this.loading = true
    const query = { id: this.$route.params.id || 0 }
    try {
      if ( !query.id ) {
        const {
          data: {
            levelList,
            customerTypeList
          }
        } = await addSeach()
        this.loadingShow = true
        this.levelList = levelList
        this.customerTypeList = customerTypeList
        this.title = '新建公司'
      } else {
        const {
          data: {
          customerTypeList,
          levelList,
          name,
          shortName,
          aptitudeType,
          aptitudeNo,
          provinceId,
          cityId,
          countyId,
          addressDetail,
          contact,
          contactTel,
          email,
          qualificationType,
          qualificationCode,
          images,
          types,
          refusedReason,
          levelCode,
          businessDirector,
          cinemas,
          approveStatus,
          validityPeriodDate,
          status
          }
        } = await queryId(query)
        this.item.name = name
        this.item.shortName = shortName
        this.item.aptitudeType = aptitudeType
        this.item.aptitudeNo = aptitudeNo
        this.item.addressDetail = addressDetail
        this.item.contact = contact
        this.item.contactTel = contactTel
        this.item.email = email
        this.item.qualificationType = qualificationType
        this.item.images = images || []
        // this.item.types = types
        if (types.length == 1) {
          if (customerTypeList[0].typeCode == types[0].typeCode) {
            this.item.types[0] = types[0]
            this.item.typearr[0] = true
          } else {
            this.item.types[1] = types[1]
            this.item.typearr[0] = true
          }
        } else {
          this.item.types = types.sort((a: any, b: any) => {
             return a.typeCode > b.typeCode ? 1 : -1
          })
          this.item.typearr = [true, true]
        }
        this.item.refusedReason = refusedReason
        this.item.businessDirector = businessDirector
        this.item.cinemas = cinemas || []
        this.item.approveStatus = approveStatus
        this.customerTypeList = customerTypeList
        this.item.validityPeriodDate = validityPeriodDate
        this.levelList = levelList
        this.area = [provinceId || 0, cityId || 0, countyId || 0]
        this.loadingShow = true
        status == 1 ? this.title = '编辑公司' : this.title = '审核公司'
      }
    } catch (ex) {
      this.handleError(ex)
    } finally {
      this.loading = false
    }
  }

  back() {
    this.$router.go(-1)
  }

  edit(dataForms: string) {
    (this.$refs[dataForms] as any).validate(async ( valid: any ) => {
    if (valid) {
      const route = this.$route.params.id || 0
      let times: any = ''
      if ( route == '0') {
        !this.item.validityPeriodDate ? times = '' : times = new Date(this.item.validityPeriodDate).getTime()
        const oldQuery = {
          ...this.item,
          validityPeriodDate: times,
          provinceId: Number(this.item.provinceId),
          cityId: Number(this.item.cityId),
          countyId: Number(this.item.countyId),
        }
        const query = clean(oldQuery)
        const array = Object.keys(query).slice(2)
        const newqQuery = slice(query, array)
        const a = {
          ...newqQuery,
          cinemas: this.cinemas
        }
        try {
          await addQuery({
            ...newqQuery,
            cinemas: this.cinemas
          })
          this.$router.go(-1)
        } catch (ex) {
          this.handleError(ex)
        }
      }
    }
    })
  }
  @Watch('area')
  watchArea(val: number[]) {
    this.item.provinceId = val[0]
    this.item.cityId = val[1]
    this.item.countyId = val[2]
  }

  imgarray(val: any) {
    this.item.images = val.map((item: any) => {
      return item.imageUrl
    })
  }

  @Watch('typeList', { deep: true })
  watchTypeList(val: any[]) {
    this.item.typeIdList = val.map(it => it.checked ? it.id : 0)
    this.item.subTypeIdList = val.map(it => it.checked ? it.subId || 0 : 0)
  }
  @Watch('item', { deep: true })
  watchitem(val: any) {
    const form = 'dataForms'
    if (val.approveStatus == 2) {
      (this.$refs[form] as any).fields.forEach((e: any) => {
        if (e.prop == 'validityPeriodDate') {
          e.resetField()
        }
      })
    } else {
      (this.$refs[form] as any).fields.forEach((e: any) => {
        if (e.prop == 'refusedReason') {
          e.resetField()
        }
      })
    }
  }
}
</script>

